
CREATE FUNCTION [dbo].[fnGetPhanTramThueTNCN](@LUONG FLOAT)  
RETURNS FLOAT(50)   
AS   
BEGIN
	DECLARE @resulst FLOAT
	SET @resulst =
		CASE 
         WHEN @LUONG < 5000000 THEN 0.05
         WHEN @LUONG >= 5000000 AND @LUONG < 10000000 THEN 0.1
         WHEN @LUONG >= 10000000 AND @LUONG < 18000000 THEN 0.15
         WHEN @LUONG >= 18000000 AND @LUONG < 32000000 THEN 0.2
		 WHEN @LUONG >= 32000000 AND @LUONG < 52000000 THEN 0.25
		 WHEN @LUONG >= 52000000 AND @LUONG < 80000000 THEN 0.3
       ELSE 0.3 END
	RETURN @resulst
END; 
GO

